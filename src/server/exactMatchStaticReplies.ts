function normalizeExactMatchKey(input: string): string {
  return input
    .replace(/\r\n/g, "\n")
    .replace(/[\u0000-\u0008\u000B\u000C\u000E-\u001F\u007F]/g, "")
    .replace(/[\u200B-\u200F\u2060\uFEFF]/g, "")
    .trim();
}

export function getExactMatchStaticReply(input: string): string | null {
  const normalized = normalizeExactMatchKey(input);
  return EXACT_MATCH_STATIC_REPLIES[normalized] ?? null;
}
